/**
 * Function to add two numbers
 * @param {number} a 
 * @param {number} b 
 * @returns {number}  
 */
function sum(a: number, b: number) {
  return a + b;
}

/**
 * Function to subtract two numbers
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function diff(a: number, b: number) {
  return a - b;
}